int s = 999, n , i, t, c;
int Count_SubArray(int[] A, int N, int D) {
  for (; n < 1 ; s = t > 0 & c == N & t % D < 1 & t < s ? t : s) {
    n = i++;
    t = c = 0;
    for (int e : A) {
      t += n % 2 * e;
      c += n % 2;
      n /= 2;
    }
  }
  return s;
}
